"""
    算数运算符
    + 加
    - 减
    * 乘
    / 除
    // 取整除
    % 取余
    ** 指数
"""
print("1 + 1 =", 1 + 1)
print("2 - 1 =", 2 - 1)
print("3 * 3 =", 3 * 3)
print("5 / 2 =", 5 / 2)
print("5 // 2 =", 5 // 2)
print("5 % 2 =", 5 % 2)
print("5 ** 2 =", 5 ** 2)

